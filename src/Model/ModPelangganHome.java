public class ModPelangganHome {
    private int id;
    private String nama;
    private int uang;
    private int jenis_uang;
    private int status;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getUang() {
        return uang;
    }

    public void setUang(int uang) {
        this.uang = uang;
    }

    public int getJenis_uang() {
        return jenis_uang;
    }

    public void setJenis_uang(int jenis_uang) {
        this.jenis_uang = jenis_uang;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    
}
